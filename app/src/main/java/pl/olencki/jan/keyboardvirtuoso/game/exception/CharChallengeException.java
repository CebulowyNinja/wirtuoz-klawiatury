package pl.olencki.jan.keyboardvirtuoso.game.exception;

public class CharChallengeException extends Exception {
    public CharChallengeException() {
        super();
    }

    public CharChallengeException(String message) {
        super(message);
    }

    public CharChallengeException(String message, Throwable cause) {
        super(message, cause);
    }

    public CharChallengeException(Throwable cause) {
        super(cause);
    }
}
